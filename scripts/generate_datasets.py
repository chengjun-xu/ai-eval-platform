def build_mmlu():
    questions = []
    idx = 0
    for category, items in MMLU_DATA.items():
        for q, choices, answer in items:
            questions.append({
                "id": f"mmlu_ext_{idx:04d}",
                "category": category,
                "question": q,
                "choices": choices,
                "answer": answer,
            })
            idx += 1
    return questions

